    Props и emit: Один из наиболее распространенных способов связи между компонентами в Vue.js. Он основан на передаче данных из родительского компонента в дочерний через свойство (props), и на передаче событий от дочернего компонента к родительскому через функцию emit. Таким образом, родительский компонент может передавать данные дочернему компоненту через свойства, а дочерний компонент может отправлять обратно события через emit.
